    public void updateFullness(int fullness) {
        //OverFlow
        if (this.fullness + fullness > 100){
            this.fullness = 100;
        }else if (this.fullness + fullness <= 0) {
            // underflow
            this.fullness = 0;
        }else {
             this.fullness += fullness;
        }
        HappinessBar.setValue(this.fullness);
    }
    
